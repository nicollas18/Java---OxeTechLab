import java.util.Arrays;

public class TiposDadosReferenciados {
  public static void main(String[] args) {

    // String é uma classe 
    String frase = "Java é muito massa!";
    System.out.println(frase);

    // Por ser uma classe conseguimos usar métodos, como o charAt(), por exemplo
    char primeirLetra = frase.charAt(0);
    System.out.println(primeirLetra);

  }
}
