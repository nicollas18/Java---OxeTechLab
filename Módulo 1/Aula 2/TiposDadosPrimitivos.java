public class TiposDadosPrimitivos {
  public static void main(String[] args) {
    // Tipos de Dados Inteiros
    byte numeroByte = 15;
    System.out.println(numeroByte);

    short numeroShort = 23563;
    System.out.println(numeroShort);

    int numeroInt = 371803032;
    System.out.println(numeroInt);

    long numero = 300312312310L;
    System.out.println(numero);

    // Tipos de Dados Decimais
    double numeroDouble = 10101904342312312331131231132331212434234233312.09;
    System.out.println(numeroDouble);

    float numeroFloat = 101019043423123123311312311323312123312.09F;
    System.out.println(numeroFloat);

    // Booleanos
    boolean javaFacil = true;
    boolean javaDivertido = false;
    System.out.println(javaFacil);
    System.out.println(javaDivertido);

    // Caracteres
    char primeiraLetra = 'A';
    char segundaLetra = 'B';
    System.out.println(primeiraLetra);
    System.out.println(segundaLetra);

  }
}